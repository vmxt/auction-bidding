    function getGoodsAndServices() {

        let _goods_and_services = [];

        $('.form-services').each( function () {

            let _name            = $(this).val();            
            let _field           = $(this).data('field');
            let _parent          = $(this).data('parent');
            let is_existing      = false;
            let is_parent        = true;

            if( _parent !== undefined ) {
                _parent = _parent.replaceAll("_", " ").toLowerCase();
                is_parent = false;
            } else {
                _parent = null;
                _field  = null;
            }

            if( !is_parent ) {
                $.each(js_supplier_services, function(k,v){

                    if( v.cat == _parent && v.name == _name ) is_existing = true;

                });
            }

            if( $(this).prop("checked") ) {

                if( !is_existing && ( _field != 'others' || _field != null ) && !is_parent ) {

                    if($(this).parent().parent().parent().siblings().children('.form-services').prop('checked')) {

                        _goods_and_services.push({
                            'cat'     : _parent , 
                            'name'    : _name , 
                            'action'  : "new"
                        });

                    }
                }                

            } else {

                if( is_existing ) {
                    if( _field != 'others' ) {
                        _goods_and_services.push({
                            'cat'     : _parent , 
                            'name'    : _name , 
                            'action'  : "remove"
                        });
                    } else {
                        _goods_and_services.push({
                            'cat'     : _parent , 
                            'name'    : _name , 
                            'action'  : "remove"
                        });
                    }
                }
                
            }

            _sections.goods_and_services = _goods_and_services;

        });

    }


